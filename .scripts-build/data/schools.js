function getSchoolById(id) {
    return exports.SCHOOLS.find(school => school.id === id);
}
/**
 * Find school by name (case-insensitive, partial match)
 */
function findSchoolByName(name) {
    const normalized = name.trim().toLowerCase();
    return exports.SCHOOLS.find(school => school.name.toLowerCase() === normalized ||
        school.name.toLowerCase().includes(normalized) ||
        normalized.includes(school.name.toLowerCase()));
}
